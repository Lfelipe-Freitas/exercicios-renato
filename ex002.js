let base = Number(prompt("Digite o valor da base do seu quadrado"))
let altura = Number(prompt("Digite o valor da altura"))
let comprimento = Number(prompt("Digite o comprimento do seu quadrado"))
let largura = Number(prompt("Digite a largura do seu quadrado"))
let perimetro = 2 * (comprimento + largura)
let area = base * altura
alert(`A área do seu quadrado é ${area}!`)
alert(`O perímetro do seu quadrado é ${perimetro}`)