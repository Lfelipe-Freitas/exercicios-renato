let base = Number(prompt("Digite o valor da base do seu retângulo"))
let altura = Number(prompt("Digite o valor da altura"))
let comprimento = Number(prompt("Digite o comprimento do seu retângulo"))
let largura = Number(prompt("Digite a largura do seu retângulo"))
let perimetro = 2 * (comprimento + largura)
let area = base * altura
alert(`A área do seu retângulo é ${area}!`)
alert(`O perímetro do seu retângulo é ${perimetro}`)